export function add(addNumber, addSecondNumber) {
    return addNumber + addSecondNumber;
}

export function sub(subNumber, subSecondNumber) {

    return subNumber - subSecondNumber
}

export function div(divNumber, divSecondNumber) {
    return divNumber / divSecondNumber
}

export function mult(multNumber, multSecondNumber) {
    return multNumber * multSecondNumber
}
